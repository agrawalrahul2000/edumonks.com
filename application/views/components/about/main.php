				<h5>Types Of Scholarships</h5>
			</div>
			<p>
				<b>Categorized as per the Scholarship&#039;s Reach/Level</b>
			</p>
			<p>
				<b>National</b>
			</p>
			<p>Students who are currently residing in India can apply for such
				scholarship. They may have a variety of other restrictions, such as
				race, major, career plans, and test scores, but they will not be
				restricted by the city, state or by the institutions ndicated on the
				student profile.</p>
			<p>
				<b>State</b><br> These awards are restricted by the applicant&#039;s
				state residency. Other restrictions may apply.
			</p>
			<p>
				<b>Local</b>
			</p>
			<p>They are primarily awarded based on the applicant&#039;s village, city
				or district. Additional eligibility requirements are generally
				listed as well.</p>
			<p>
				<b>Institutional</b>
			</p>
			<p>These awards are given only to students who attend or plan to
				attend specified colleges and/or universities. Additional
				eligibility requirements are generally listed as well.</p>
			<p>
				<b>Global (International)</b>
			</p>
			<p>This category is filled with international student awards that
				have no residency, citizenship or institutional requirements. Other
				restrictions may apply.</p>
			<p>
				<b>Location-Specific (International)</b>
			</p>
			<p>These awards are restricted either by the applicant&#039;s citizenship
				or country residency.</p>
			<p>
				<b>Institutional (International)</b><b></b>
			</p>
			<p>These awards are given only to international students who attend
				or plan to attend specified colleges and/or universities anywhere in
				world except India. Additional eligibility requirements are
				generally listed as well.</p>
			<p>
				<b>Categorized as per Provider Type</b>
			</p>
			<p>
				<b>Government</b>
			</p>
			<p>Scholarships that are sponsored by the Government at National,
				State or Local level.</p>
			<p>
				<b>NGO</b>
			</p>
			<p>These scholarships are sponsored by Registered Non-Government
				Organizations.</p>
			<p>
				<b>Academic Institution or Academic Bodies</b>
			</p>
			<p>They are given by formal academic institutions like schools,
				colleges, etc. or Academic bodies/groups like CBSE, UGC, etc.</p>
			<p>
				<b>Corporate or Corporate Bodies</b>
			</p>
			<p>These are sponsored by companies usually as part of their CSR
				activities or by Corporate bodies/Group like FICCI, NASCOM, etc.</p>
