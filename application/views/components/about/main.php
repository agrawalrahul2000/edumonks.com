				<h5>What is Scholarship</h5>
			</div>
			<p>
				The term &quot;Scholarship&quot; here refers to An&nbsp;<b>Award&nbsp;</b>granted
				to a person by&nbsp;<b>evaluation&nbsp;</b>of their skills on the
				basis of pre-defined criteria. The scholarship may be granted for
				Academic, Co-Curricular and Extra-curricular achievements/skills.
			</p>
			<p>Award should include atleast one of the following:</p>
			<p>1)&nbsp;&nbsp;&nbsp;&nbsp; Cash Prize</p>
			<p>2)&nbsp;&nbsp;&nbsp;&nbsp; Admission to an institute if
				accompanied by a Fee Waiver</p>
			<p>3)&nbsp;&nbsp;&nbsp;&nbsp; Trip</p>
			<p>4)&nbsp;&nbsp;&nbsp;&nbsp; Internship</p>
			<p>5)&nbsp;&nbsp;&nbsp;&nbsp; Gift</p>
			<p>Evaluation can be done by:</p>
			<p>1)&nbsp;&nbsp;&nbsp;&nbsp; Proof of any past achievements like
				Marks, winning of a competition, recommendation, etc.</p>
			<p>2)&nbsp;&nbsp;&nbsp;&nbsp; An examination or competition
				specifically for this purpose.</p>
			<p>
				<i>Note: Competitions that are conducted by academic institutions
